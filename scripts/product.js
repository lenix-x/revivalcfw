document.querySelectorAll('.js-add-to-cart').forEach((button) => {
    button.addEventListener('click', () => {
    const productId = button.dataset.productId;

    let validItem

    cart.forEach((item) => {
      if (productId === item.productId) {
        validItem = item
      }
    })
    if (validItem) {
      validItem.quantity += 1
    } else {
      cart.push({
        productId: productId,
        quantity: 1
      })
    }
    console.log(cart)
  })
})

